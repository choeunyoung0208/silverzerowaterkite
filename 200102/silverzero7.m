%200103 : 찾아본 결과 f와 g의 데이터 클래스는 uint8이므로 범위 [0, 255]의 무부호 8비트(1바이트) 정수임. 따라서 uint8에서 a라는 값이 있으면 a의 보수는 255-a가 됨을 f와 g의 uint8 데이터 들을 비교해 보고 확인할 수 있었음
